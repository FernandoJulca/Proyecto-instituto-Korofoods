CREATE TABLE TB_PLATO (
    ID_PLATO SERIAL PRIMARY KEY,
    NOMBRE VARCHAR(100) NOT NULL,
    PRECIO DECIMAL (10,2) NOT NULL,
    STOCK INT NOT NULL DEFAULT 0,
    TIPO_PLATO CHAR(1) NOT NULL,
    IMAGEN VARCHAR(150) NULL,
    ACTIVO BOOLEAN DEFAULT TRUE,
    CONSTRAINT TP_PLATO_CHECK CHECK (TIPO_PLATO IN ('E','S','P','B'))
);

CREATE TABLE TB_ETIQUETA (
    ID_ETIQUETA SERIAL PRIMARY KEY,
    NOMBRE VARCHAR(50) NOT NULL UNIQUE,
    DESCRIPCION VARCHAR(100),
    ACTIVO BOOLEAN DEFAULT TRUE,
    FECHA_REGISTRO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE TB_PLATO_ETIQUETAS (
    ID_PLATO_ETIQUETA SERIAL PRIMARY KEY,
    ID_PLATO INT NOT NULL,
    ID_ETIQUETA INT NOT NULL,
    ACTIVO BOOLEAN DEFAULT TRUE,

    FOREIGN KEY (ID_PLATO) REFERENCES TB_PLATO(ID_PLATO),
    FOREIGN KEY (ID_ETIQUETA) REFERENCES TB_ETIQUETA(ID_ETIQUETA),

    UNIQUE (ID_PLATO, ID_ETIQUETA)
);
